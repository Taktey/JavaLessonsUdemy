package com.javalessons.oop.classobjectmethods;

public class Dog {
    private String name;
    private int age;
    private String breed;
    private boolean aggressive;

    public Dog(String name, int age, String breed, boolean aggressive) {
        this.name = name;
        this.age = age;
        this.breed = breed;
        this.aggressive = aggressive;
    }
}
